let _strings = {};
let _currentLang = 'tr';

function __(key, ...args) {
  let s = _strings[key];
  if (s === undefined) s = key;
  if (args.length) args.forEach((a, i) => { s = s.replace(new RegExp('\\{' + i + '\\}', 'g'), a); });
  return s;
}

function setLanguage(lang) {
  _currentLang = lang;
  _strings = _translations[lang] || _translations.tr;
  document.documentElement.lang = lang;
  document.querySelectorAll('[data-i18n]').forEach(el => { el.textContent = __(el.dataset.i18n); });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => { el.placeholder = __(el.dataset.i18nPlaceholder); });
  document.querySelectorAll('[data-i18n-title]').forEach(el => { el.title = __(el.dataset.i18nTitle); });
  document.querySelectorAll('[data-i18n-value]').forEach(el => { el.value = __(el.dataset.i18nValue); });
  const sel = document.getElementById('langSelect');
  if (sel) sel.value = lang;
  localStorage.setItem('cabinLang', lang);
  const ev = new Event('languageChanged');
  document.dispatchEvent(ev);
}

function getLanguage() { return _currentLang; }

